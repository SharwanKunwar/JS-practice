//Demonstrate the use of switch case statements in js

let opt = 7;

switch(opt){
    case 1:
        console.log("sunday");
        break;

    case 2:
        console.log("monday");
        break;
        
    case 3:
        console.log("tuesday");
        break;
        
    case 4:
        console.log("wednesday");
        break;

    case 5:
        console.log("Thrusday");
        break;
    
    case 6:
        console.log("Friday");
        break;

    case 7:
        console.log("Saturday");
        break;

    default:
        console.log("end");
}